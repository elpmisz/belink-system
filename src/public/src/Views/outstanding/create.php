<script>
  initializeSelect2(".order-select", "/payment/order-select", "-- รายชื่อเลขที่สัญญา --");

  $(document).on("change", ".order-select", function() {
    const order = ($(this).val() || '');

    if (order !== '') {
      axios.post("/outstanding/get-item-view", {
          order,
        })
        .then((res) => {
          let result = res.data;
          let tableContent = '';
          if (result.length > 0) {
            tableContent = `
              <tr>
                <th width="10%"><input type="checkbox" id="check_all"></th>
                <th width="50%">รายละเอียด</th>
                <th width="10%">จำนวน</th>
                <th width="10%">หน่วยนับ</th>
                <th width="10%">จำนวนเงิน</th>
              </tr>
            `;

            result.forEach((item, index) => {
              const estimate = parseFloat(item.estimate).toLocaleString('th-TH', {
                minimumFractionDigits: 2,
                maximumFractionDigits: 2
              });
              tableContent += `
                <tr class="item-tr">
                  <td class="text-center">
                    <input type="checkbox" name="item_index[]" value="${index}">
                  </td>
                  <td class="text-left">${item.name}</td>
                  <td class="text-center">${item.amount}</td>
                  <td class="text-center">${item.unit}</td>
                  <td class="text-right">${estimate}</span></td>
                </tr>
              `;
            });

            $(".items-div").show();
          } else {
            $(".items-div").hide();
          }

          $(".items-table").html(tableContent);
        }).catch((error) => {
          console.log(error);
        });
    }
  });

  $(".file-decrease").hide();
  $(document).on("click", ".file-increase", function() {
    let row = $(".file-tr:last");
    let clone = row.clone();
    clone.find("input, select").val("").empty();
    clone.find("span").text("");
    clone.find(".file-increase").hide();
    clone.find(".file-decrease").show();

    clone.find(".file-decrease").off("click").on("click", function() {
      $(this).closest("tr").remove();
    });

    row.after(clone);
  });

  $(document).on("change", "input[name='file[]']", function() {
    const file = $(this).val();
    const size = ($(this)[0].files[0].size / (1024 * 1024)).toFixed(2);
    const extension = file.split(".").pop().toLowerCase();
    const allowedExtensions = ["png", "jpeg", "jpg", "pdf", "doc", "docx", "xls", "xlsx"];

    if (size > 5) {
      Swal.fire({
        icon: "error",
        title: "ไฟล์เอกสารไม่เกิน 5 Mb!",
      });
      return $(this).val("");
    }

    if (!allowedExtensions.includes(extension)) {
      Swal.fire({
        icon: "error",
        title: "เฉพาะไฟล์นามสกุล JPG, PNG, WORD และ EXCEL เท่านั้น",
      });
      return $(this).val("");
    }
  });

  $(".date-select").daterangepicker({
    singleDatePicker: true,
    showDropdowns: true,
    locale: {
      "format": "DD/MM/YYYY",
      "applyLabel": "ยืนยัน",
      "cancelLabel": "ยกเลิก",
      "daysOfWeek": [
        "อา", "จ", "อ", "พ", "พฤ", "ศ", "ส"
      ],
      "monthNames": [
        "มกราคม", "กุมภาพันธ์", "มีนาคม", "เมษายน", "พฤษภาคม", "มิถุนายน",
        "กรกฎาคม", "สิงหาคม", "กันยายน", "ตุลาคม", "พฤศจิกายน", "ธันวาคม"
      ]
    },
    "applyButtonClasses": "btn-success",
    "cancelClass": "btn-danger"
  });

  $(".date-select").on("apply.daterangepicker", function(ev, picker) {
    $(this).val(picker.startDate.format('DD/MM/YYYY'));
  });

  $(".date-select").on("keydown paste", function(e) {
    e.preventDefault();
  });

  $(document).on("change", "#check_all", function() {
    $("input:checkbox").prop('checked', $(this).prop("checked"));
  });
</script>